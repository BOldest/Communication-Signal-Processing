function [gret] = Gaus(co_m,rec)
  mret=horzcat(co_m,rec);
  size(mret)
  [m,n]=size(mret); % m = rows , n= column
  
  for j=1:m-1 
      for z=2:m
        if (j<= n-1)
          if mret(j,j)==0
              temp=mret(j,:);mret(j,:)=mret(z,:);
              mret(z,:)=temp;
          end
         end 
      end
      for i=j+1:n-1
          mret(i,:)=mret(i,:)-mret(j,:)*(mret(i,j)/mret(j,j));
      end
  end
  x=zeros(1,n-1);
  for s=n-1:-1:1
      c=0;
      for k=2:n-1
          c=c+mret(s,k)*x(k);
      end
      x(s)=(mret(s,n)-c)/mret(s,s);
   
  end
  gret=x';
endfunction


